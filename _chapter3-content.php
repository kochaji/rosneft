					</p>
					<p>В итоге в начале 1937 года Служба горючего Красной Армии приняла на вооружение сборно-разборный трубопровод так называемого «складского» применения. Он был гораздо короче, чем даже двухкилометровый опытный модуль Шинберга-Хромова — всего полтора километра, и все же это был уже шаг вперед.
