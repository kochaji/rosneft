					<p>Развитие технического оснащения Красной Армии в начале 30-х годов потребовало резкого улучшения снабжения моторизованных частей топливом. Военные интересовались всеми новинками, и не удивительно, что уже в 1933 году Управление по снабжению горючим заказало тресту «Нефтепроводстрой» разработку быстросборного (и столь же быстроразборного) трубопровода для перекачивания горючего в полевых условиях. Работа была поручена молодым специалистам Шинбергу и Хромову, под руководством управляющего трестом Георгия Рогачева.
					</p>
