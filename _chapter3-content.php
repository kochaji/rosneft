					</p>
					<p class="blockquote">Оказалось, что слова «склад на линии фронта» надо было понимать буквально. Не успел инженер Шинберг с сопровождающими начать обмер труб, как тут же начался артиллерийский обстрел. Сопровождавший Шинберга кладовщик погиб. На глазах новенькие трубы силой взрыва закручивало спиралью и секло осколками. Было ясно, что работать придется лишь по ночам и с крайней степенью аккуратности.
