					Рождение идеи
					</h2>
					<p>Зимой 41-42 ого года войска Волховского фронта удерживали на западном берегу реки Волхов важные плацдармы, для которых доставлялось топливо по льду. Однако с началом весны лед стал таять, и военные заправщики собрали оригинальный трубопровод. Они использовали трубы из «складских» трубопроводов, которые соединили с прорезиненными гибкими рукавами. Так им удалось решить проблему перекачки топлива через Волхов, причем в условиях постоянного огневого противодействия противника.
					</p>
					<p>Поэтому сама идея создания трубопровода, проложенного по дну Ладоги несомненно приходила в голову многим людям, связанным с транспортировкой нефтепродуктов. Такой трубопровод мог работать в любое время года, круглосуточно, и был гораздо менее уязвим для ударов противника. Но одно дело благие пожелания, и совсем другое — практическое решение. Ведь на Ладоге требовалось проложить настоящий, многокилометровый трубопровод. Здесь требовались серьезные подводные изыскания по трассе, чтобы выяснить геологические и гидрологические условия на трассе.
					</p>
					<p>Лучше всего знали обстановку на дне Ладоги специалисты ЭПРОН — знаменитой в свое время Экспедиции подводных работ особого назначения. Именно они проложили по дну Ладоги телефонный кабель для связи города с Москвой, они же придумали уникальную технологию перевозки нефтепродуктов вплавь в железнодорожных цистернах. То есть цистерны подгонялись по железной дороге к кромке воды. Рельсы уходили в глубь озера, и цистерны спокойно начинали качаться на волнах, поднявшись над своими колесными тележками. «Поезд» из качающихся на волнах цистерн брал «за усы» буксир, и тянул его на другой берег Ладоги, где они еще под водой становились на рельсы и вытягивались на железную дорогу. Так что эпроновцам фантазии было не занимать.
					</p>
					<p>Этими работами руководила первая женщина-водолаз СССР Нина Соколова. Выпускница Ленинградского института водного транспорта, инженер Соколова не просто хотела с поверхности руководить водолазами — но и лично участвовать в подводных работах. Свою подводную карьеру она начала в Сочи, еще в 1937 году.
					</p>
					<p>Сейчас, в эпоху аквалангов, трудно представить себе, как было сложно и опасно работать в тяжелом водолазном снаряжении. Водолаз не парил в водной среде, да и пользы от этого было мало - он вставал на дно, производил сложную и тяжелую работу. Руководили работами инженеры, которые сидели на поверхности и рассказывали водолазам, что надо делать. Нина Соколова опускалась под воду сама, в отличие от своих сухопутных коллег-инженеров, видела подводные объекты «на месте» и лучше многих знала пределы возможного.
					</p>
					<p>Так что к весне 1942 года Соколова уже числилась опытным специалистом, к мнению которого прислушивались “на самом верху”. Эта молодая и с виду хрупкая женщина вдобавок обладала твердым характером и, как сейчас говорят, креативным мышлением. И вот однажды, еще в феврале 1942 года, летя в самолете на одно из совещаний в Москву, она  поделилась своей идеей с вышестоящим начальством. По ее мнению,  водолазы вполне могли обеспечить монтаж такого трубопровода в кратчайшие сроки. Вопрос был в том, как монтировать трубопровод.
