					<p>	И все же — Фарман Салманов не был авантюристом, который искал нефть «сам по себе». Это был в высшей степени советский человек, который добывал нефть для своей огромной страны. И действовал он также в интересах страны. Тем более что он был профессионалом, хоть по современным меркам и очень молодым.
					</p>
					<p>	В 1955 году, в возрасте 24 лет, он уже был начальником Плотниковской и Грязненской нефтегазоразведочных экспедиций, которые оперировали в Кемеровской и Новосибирской областях. Секрет карьеры прост — война выкосила специалистов, и командовать приходилось тем, кто имел знания и был готов взвалить на себя груз ответственности. Стоит вспомнить того же Николая Байбакова, который стал наркомом (министром) нефтяной промышленности в 31 год.
					</p>
					<p>	К 1957 году у Салманова уже сложилась вполне серьезная репутация умелого начальника, и знающего специалиста. А то, что разведочное бурение, которое проводилось в ранние 50-ые годы не дало результата в Среднеобье, Салманов относил к ошибкам выбора места, слабой организации работ и недостаточному техническому снабжению. И – воспользовался служебным положением, которое давало ему весьма широкие полномочия.
					</p>
					<p>	Полулегальное перемещение экспедиции к сургутским берегам было обстоятельно подготовлено — это не было случайным броском. Заранее договорились с речниками, были получены баржи и буксир, загружено оборудование для строительства жилья для буровиков и членов их семей, пилорама, кузня и многое другое, что нужно для обустройства на новом месте экспедиции в составе более ста человек.
					</p>
					<p>	Руководство вскоре узнало о самоволии Салманова, но все же не стало раскручивать репрессивный механизм. Салманов базировался на теории Губкина, имя которого для нефтяников было весомым, а с другой стороны — два года безуспешных поисков нефти на Кузбассе уже позволяли задавать вопрос о том, на что тратятся народные деньги. В свою очередь напористому Салманову могло и повезти. Сейчас уже трудно сказать, какие доводы возобладали (есть разные версии) — но вышестоящее руководство оформило задним числом распоряжение о переброске экспедиции Салманова в Сургут. Было это в августе 1957 года.
					</p>
