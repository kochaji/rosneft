					</h2>
					<p>	А в 1961 году беседа с Хрущевым практически сразу дала свои плоды. Откуда не возьмись появились средства, и уже спустя полгода в экспедиции Салманова работала почти тысяча человек. Этому способствовало положительное решение специальной комиссии во главе с Николаем Байбаковым, который приехал инспектировать новооткрытое месторождение. Именно он дал команду «брать нефть».
					</p>
					<p>	Для того, чтобы ее взять, пришлось прибегнуть к поддержке местных партийных органов в лице товарища Протазанова, благодаря которому на Тюменском судостроительном заводе согласились построить 20 нефтеналивных барж. Каждая баржа имела водоизмещение в 2000 тонн. И 26 мая 1964 года первая баржа встала под загрузку промышленной нефтью Западной Сибири. Она пошла далее по Иртышу к нефтеперерабатывающим предприятиям. В первый год, по воспоминаниям Салманова, месторождения дали 100 тысяч тонн нефти, а уже через три года объем вырос в три раза, а местный нефтеналивной флот вырос до ста судов различной вместимости. Стало ясно, что Сибирь требует развитой системы трубопроводов.
					</p>
