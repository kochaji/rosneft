					<h2 class="h2 title title--start js-title" id="siberia">
					Legends of Siberia
					</h2>
					<p>
					As a young boy Farman heard his uncle's stories about the astonishing land of Siberia: the astounding beauty of this far-away place and the riches this magical land boasted. 					</p>
					<p>
					This was the early 1930s, when literally every day newspapers and radio reported on the latest successes of the Soviet people. Exploration of the Arctic and the expanses of Siberia was a state affair. Not many people realised that behind the reports of derring-do there lay an entirely different truth … Farman found this out when in 1937 his father was arrested. 					</p>
					<p>
						Aged only six Farman became his mother's main support as the eldest child of the family. The need to earn some money did not extinguish his interest in the mysteries of Siberia. He asked his uncle, a schoolteacher, if he could find him books on Siberia and the Far East.
					</p>
					<p>
					Through his dealings with uncle Suleiman Farman knew Russian better than all the other schoolchildren, and could therefore read literature that was quite complicated. His uncle brought from Baku some books by Vladimir Obruchev, a well-known Russian geologist and exponent of popular science, which were devoted to studying Siberia. The curiosity of a child began to change to the interest of an adolescent and then young man who was growing up in very difficult conditions. 					</p>
					<p>
					In 1941 uncle Suleiman died. Soon afterwards the Great Patriotic War began. Children also helped adults as much as they could. What is surprising is that after study and work Farman still had the energy to visit a natural history study group and go on hikes in the mountains in order to collect samples of minerals and rock formations. Many years later Farman Salmanov would talk about how happy he was when he brought home to his mother pieces of quartz which would give off bright sparks when struck, and this primitive flintstone could be used instead of matches, which were in terribly short supply at the time.					</p>
					<p>
					During the War Farman realized just how important oil was for the country. Oil in Azerbaijan was not just a useful fossil fuel but also part of that land's ancient history. Working in the oilfields was something natural for a young Azerbaijani lad. Butu Farman was deep in thought about where else on the territory of the Soviet Union there might be oil deposits. In the Volga or Urals regions, the Komi peninsular,… where else?					</p>
