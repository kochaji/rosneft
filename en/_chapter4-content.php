					<h2 class="h2 title js-title " id="youth">
					Boyhood and youth
					</h2>
					<p>
					In 1946,a year after the end of the Great Patriotic War, elections were called for the Supreme Soviet of the Soviet Union. Despite the absence of any real political alternative in the elections, there was considerable individual ambition, which spurred candidates to travel around constituencies in order to listen to the "mandate of the people". 					</p>
					<p>
					The candidate for the constituency of Shamkhor region was Nikolay Baybakov, the eminent People's Commissar for the oil industry. At that time he was only 35 years old. Baybakov was a favoured son in Azerbaijan: he had been born in 1911 near Baku, on the Apsheron peninsular in the small settlement of Sabunchi. In 1942 he had been entrusted by Stalin to organize the evacuation and if necessary the destruction of the North Caucasian oilfields, given the following alternatives: "if you leave the working wells for the Germans intact you'll be shot, and if you disassemble the equipment prematurely you'll also be shot". Baybakov carried out the command of the Supreme Commander-in-Chief and many other instructions. Thus, the candidate had an excellent reputation: if he promised something, he would do it. 					</p>
					<p>
						That is why on the eve of Baybakov's arrival fifteen-year-old Farman, as the best Russian speaker, was given the task of speaking before the esteemed visitor to tell him about the needs of the village and the school.
					</p>
					<p>
					Salmanov was very nervous and in the end blurted out what he thought was needed off his own bat: an asphalt road to the school and lighting for it. And in everyone's presence he added that he would most certainly become an oil geologist. Baybakov promised to resolve everything (and apparently kept his word), and then said that Farman's choice of career was the correct one as the country needed oil, the "second Baku" was taking shape in the Volga and Ural regions, and Siberia would be the next port of call...					</p>
					<p>
					After graduating from school, Salmanov enrolled in a petroleum institute. There he found himself at the epicentre of discussions on the nature of oil and the means of opening up the boundless expanses of Siberia. The truth was, however, that he was not being sent to Siberia, and Farman Salmanov appealed for the second time to the all-powerful Baybakov in a private letter, asking for assistance in being posted to Western Siberia. Baybakov helped here, too. Salmanov saw with his own eyes that the exploration of Siberia was not only scientific romanticism, but also the clash of theories, interests and ambitions. 					</p>
