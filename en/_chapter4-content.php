						<h2 class="h2 title js-title" id="runaway">
						Escape from Kuzbass
						</h2>
						<p>
						Many sources describe Farman Salmanov's famous navigation to the banks of Surgut along the river Ob as a real pirate raid. He commandeered a barge, loaded on to it his drilling equipment, moored on the riverbank and began drilling. Sure enough, the long-awaited Siberian oil poured forth, he sent a telegramme to Khrushchev and was soon world-famous. That's not quite what happened. It's probably logical that legends don't need boring production details. All the more as Salmanov sailed off with his colleagues at his own peril and risk. He could have been dismissed from his work, even put in prison. 					</p>
						<p>
						But still Farman Salmanov was no adventurer looking for oil "per se". He was a Soviet citizen to the highest degree and was digging for oil for his the sake of his vast country. He was also acting in the interests of his country. Especially as he was a professional, if still quite young by contemporary standards. 					</p>
						<p>
						In 1955, aged 24, he was already the head of the Plotnikov and Gryaznensk oil and gas exploratory expeditions which were operating in the Kemerovo and Novosibirsk districts. The secret of his success was simple: the War had wiped out the specialists and those who had the knowledge and were ready to take on the weight of responsibility got the chance to take over. We should remember Nikolay Baybakov here, who became People's Commissar (Minister) for the oil industry aged only 31. 					</p>
						<p>
						By 1957 Salmanov had already carved out a serious reputation as a skilled leader and a hot-shot specialist. The fact that exploratory drilling in the early 1950s brought no results in the Central Ob region Salmanov put down to the wrong choice of location, poor work organization and insufficient equipment. He also made use of his official status, which provided him with very broad powers.					</p>
						<p>
						The semi-legal relocation to the banks of Surgut had been prepared in advance, it was not an impulsive dash. He had agreed beforehand with the local rivermen and received barges and a tugboat, loaded on board equipment for the construction of housing for the drillers and their families, a sawmill, a forge and much more besides that was needed to develop the infrastructure of a new site for more than one hundred people. 					</p>
						<p>
						The leadership soon found out about Salmanov's willful actions, but decided against setting in motion any repressive measures. Salmanov was inspired by Gubkin's theories, whose name carried much weight for oil workers, but on the other hand two years unsuccessfully looking for oil in Kuzbass enabled much thought to be given to what public money was being wasted on. The self-assertive Salmanov was entitled to some luck. It is hard now to say which arguments prevailed (there are various versions), but the powers-that-be formalized post factum the instruction to redeploy Salmanov's expedition to Surgut. That was in August 1957. 					</p>
