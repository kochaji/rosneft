						Money, fame, respect and the admiration of society are all important, but the feeling of being a "pioneer" is difficult to put into words. And each person who chose the road to Siberia could count on "washing in the first oil from the fountain".					</p>
						<p>
						The arts world and the best journalists also championed what people would normally run away from: substandard living conditions, a primitive lifestyle, etc. Pine nameplates, communities of tents, ships  sailing up and down the rivers of Siberia, full of young oil workers and construction workers, all this was new and wonderful. Sadly, in practice it became the norm to firstly build the industrial sites (in other words, completely the opposite of how Salmanov began work in Surgut) and only then, under the infamous "leftover principle", to build accommodation and social amenities such as kindergartens, schools, houses of culture, polyclinics and hospitals. 					</p>
						<p>
						This was the person, for whom the romance of exploration was uppermost, who was considered worthy of the admiration of his peers, the Party, and the Soviet people as a whole. Higher education institutes specializing in geology experienced fierce competition for places, possibly on a par with physics faculties, theatre or flying schools. It was only years later that many of the graduates understood the reason why the country needed so many specialists in geology. The reason wasn't only in the huge amount of work, but also the turnover of personnel. Accidents and disease brought on by the work took their toll of the specialists and workers.  Though it has to be said that the vast majority of young people at that time paid no heed to that. They believed that they were doing something important and necessary, and that their children would thank them for what they had achieved. 					</p>
