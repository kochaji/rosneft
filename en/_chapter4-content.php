						Farman Salmanov was a representative of that generation of Soviet citizens whose lot was to lift up a ruined country, and achieve wondrous success. The first Sputnik, the first man in space, the first atomic ice-breaker and the world's first atomic power station. The world's largest hydro-electric power stations and, last but not least, the huge Western Siberian oil province. 					</p>
