						A boy from a small village in Azerbaijan could in all probability become involved in the oil industry in the oilfields of Baku. But life has its own ways, and he found himself prospecting for oil in Siberia, where he in fact discovered it. His name became a symbol for the most romantic period of Soviet history, when physicists, lyric poets and geologists seemed indeed to be creating a new society.						</p>
						<!-- *** -->
						<p>
						The history of great discoveries is above all the history of those people who make them, and this in turn is often a succession of chance events. The fate of this boy from the small village of Morul in the northern foothills of the Lesser Caucasus was largely determined by one act carried out by his uncle Suleiman. In line with family traditions, Suleiman was entrusted to go to the town of Ganja with a part of his already small herd as an offering to the local mosque. Suleiman objected to this, and the mullah arrived to deal with the recalitrant young man. Suleiman now did something else that changed not only his life, but the fate of his descendants - he set his dog on the mullah.						</p>
						<p>
							In the Russian Empire respect for traditional religions was a very serious matter, and Suleiman's act was punished in the most severe manner. The seventeen-year-old youth was sentenced to 20 years of hard labour. In 1888 he set off for Siberia.
						</p>
						<p>
						In the end, there was nowhere in Siberia that Suleiman's tracks couldn’t be found. Word had it that he even got as far as Sakhalin, which was at that time the centre for hard labour in the Far East, and even saw and associated with the great Russian writer Anton Chekhov there. The years went by, and Suleiman got used to life in this astonishing land. He learned Russian and the traditions of the Siberians, he liked this rugged but stunning landscape, the customs of the local people, representatives of the most diverse peoples settling in this amazing land, whether it be by their own free will or otherwise. 						</p>
						<p>
						With the beginning of the Russo-Japanese War came a chance to reduce his sentence, and Suleiman signed up as a volunteer. For his bravery in battle he received several honours, the most important of which was his freedom. It was here that he met the love of his life, a Russian woman called Olga, who travelled back with him to the Kura valley and took the name Feruza. She bore Suleiman nine children, including the father of our hero, Kurban. And on 28 June 1931 Kurban was delighted to see his own first son come into the world. This was Farman Salmanov, the man who would change the history of this huge country.						</p>
