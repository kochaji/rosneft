					<h2 class="h2 title js-title " id="oil">
					On the nature of oil
					</h2>
					<p>
						В 1910 году Иван Михайлович Губкин заканчивает Санкт-Петербургский горный институт. В отличие от своих сокурсников он выглядел уже «стариком» - ведь на момент окончания института ему было уже 39 лет. Но он свое позднее вступление на геологическую стезю компенсировал совершенно грандиозным трудолюбием и феноменальной интуицией.
					</p>
					<p>
						The history of how two theories on the origins of oil developed is worthy of a whole novel. To keep a long story short, however, for more than a hundred years two theories have been in conflict: non-organic and organic. The non-organic theory, which Dmitry Mendeleev adhered to in his lifetime, assumes that oil is constantly formed in the course of deep-lying processes at the earth's core and in the course of a whole number of successive chemical reactions.  					<p>
							<p>The organic theory of the origin of oil was actively supported by Ivan Gubkin, Russia's most eminent oil expert, who believed that oil was formed as a result of the decomposition of the remains of plants and living beings. 					</p>
						</p>
						<p>
						Depending on which theory you believe to be the correct one, the strategy of looking for deposits is then formed logically. In very approximate terms, we can say that the "non-organicists" were of the opinion that oil in Western Siberia should be looked for in, for example, the foothills of the Ural mountains or in Kuzbass. 					</p>
						<p>
						"The organicists", however, believed the vast plains to have more potential, such as the central part of Western Siberia where since time immemorial oil and gas had been observed escaping to the surface as sedimentary material had been constantly built up there that contained a large amount of bio-organic substrate from the Jurassic and Cretaceous periods, when this territory was the bottom of a shallow sea.  					</p>
						<p class="blockquote">
						The relations between the two schools were very confrontational. The same data were interpreted differently by representatives of the different schools. This was called "demonstrating fidelity to one's principles". Scientific disagreements were further compounded by the economic interests of the different organizations, as well as personal ambition. 					</p>
						<p>
						Furthermore, drilling work in the central part of the Western Siberian plain, in the area of the Ob river, was very expensive, much more expensive than on the plain's periphery, the Kuzbass or the foothills of the Urals. Also, there was the traditional bureaucratic leapfrogging as some industrial geological institutions opened while others closed. 					</p>
						<p>
						Farman Salmanov was himself a firm supporter of the organic theory and even devoted his diploma project to something that officially did not exist: the oil-bearing region of the Central Ob area. Despite the objections of his professors, he insisted on retaining his own personal conclusions in the text. The obstinate young man was sent to the place where oil was supposed to be found: Kuzbass. In his own words, he wasted two years there drilling in vain where there was no oil and no prospect of oil. Salmanov did not keep his counsel and wrote to various authorities complaining that he was engaged in a totally hopeless undertaking. The matter reached the District Committee, which was a very serious affair as Party bodies in Soviet times held a great swathe of powers. Farman Salmanov evidently felt that his hour had come. He made his famous "escape".					</p>
