						Those who followed the fog…
						</h2>
						<p>
						The death of Stalin in 1953 and the subsequent amnesties and rehabilitations put an end to the practice of using the work force of the Main Directorate of Camps (GULag). Most convicts were released. But the country still needed many young working hands. And what was no longer possible to get through coercion proved popular by using young people's natural enthusiasm. The opening up of the North and Siberia, which started after the discovery of the Surgut oil, became possible because the younger generation wanted it. 					</p>
						<p>
						Opening up the virgin lands, damming the Siberian rivers, extracting Siberian oil were key moments in the development of a mass youth movement. It was the world of young, healthy people, truly the "radiant new world". Joy filled millions of hearts. The authorities did everything to stop that flame from going out. 					</p>
