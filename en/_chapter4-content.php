						<h2 class="h2 title title--start js-title" id="victory">
						Triumph
						</h2>
						<p>
						Only with the next navigation was the equipment delivered and what is known as orientation drilling began. The chorus of sceptics got louder and louder, foretelling a sad end to the Salmanov saga. Rumours circulated that soon his case would be taken up by the public prosecutor for the inappropriate use of state funds. Even so, those funds were not enough, and occasionally the oilmen had to resort to some strange "barters". On one occasion one and a half tons of steel cable had to be delivered from Surgut to the drill sites, but there was no transport. An agreement was brokered with the chiarman of a local collective farm who provided horses to haul the cable by pulling, but in exchange the geologists had to reshoe them all. 					</p>
						<p>
						1959 had come and gone and 1960 was coming to an end. The exploratory wells had not yet given out any oil. Each new day the voices of the sceptics grew stronger, calling for the work to be terminated. The geological exploratory mission had its funding cut, and instructions came from Moscow to wind down the work. Then Salmanov gathered his team together and proposed drilling another couple of boreholes "for good luck", though there was nothing fortuitous about it as there was a good chance of striking oil. The best chances were in the Megion field near Nizhnevartovsk. In the course of drilling two horizons had already been opened, but they gave out only small spurts, not quite what was needed. Something similar had been observed previously, before Salmanov's arrival. What was needed was a stable and abundant flow of oil. Finally, a decision was reached to open up one more potential horizon. 21 March 1961 a fountain was unleashed. 					</p>
						<p>
						There are many versions of the famous telegrammes Salmanov sent to every possible level of manager and authority. To his scientific opponents he wrote: "Dear comrade, in Megion at well no. 1 an oil fountain from a depth of 2,180 metres is gushing forth. Is it all now clear? Respectfully, Farman Salmanov". To the Directorate of Tyumen Geology he sent a radio telegramme with the message "The well is gushing in due fashion!", and to Nikita Khrushchev "I have found oil! That's that! Salmanov". 					</p>
						<p>
						There are also different versions of what happened next in those hours and days. Witnesses told of spontaneous rejoicing in a local club which spilled over into just such a spontaneous rally much enlivened by the champagne Salmanov had delivered. What is factually accurate is that Salmanov also reported the discovery to Alexander Protazanov, chairman of the Tyumen Regional Executive Committee. According to Salmanov, Protazanov, though himself a mining engineer by training, did not grasp the full import of what was happening, but cottoned on to the quite lengthy report and made arrangements to fly to Moscow and report to the relevant authorities. By the time he was due to fly out it was already clear that the Megion oil was high quality, and the fountain was still gushing from the well. 					</p>
