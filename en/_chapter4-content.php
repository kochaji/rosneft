						The path of oil
						</h2>
						<p>
						In 1961 the conversation with Khrushchev almost immediately paid dividends. From out of the blue resources appeared and six months later Salmanov's expedition numbered almost a thousand people. This was down largely to the positive decision made by the special commission headed by Nikolay Baybakov, who had come to inspect the newly discovered fields. It was he who gave the command "get the oil". 					</p>
						<p>
						But to get it required the support of the local Party bodies in the person of Comrade Protazanov, thanks to whom it was agreed to build 20 oil-carrying barges at the Tyumen shipbuilding plant. Each barge had a displacement of 2000 tons. On 26 May 1964 the first barge loaded the industrial oil of Western Siberia. It travelled down the Irtysh river to the oil refining facilities. Salmanov recalls that in the first year the fields provided 100 thousand tons of oil, after three years that amount grew threefold, and there were then a hundred local oil-carrying barges. It became obvious that Siberia needed a well-developed system of pipelines. 					</p>
						<p>
						Years later Farman Salmanov tried to come to terms with the fact that the availability of such wonderful resources was partly to blame for the onset of the subsequent "stagnation". He came to the conclusion that their emergence coincided with the stalling of the Soviet economy in the late 1960s. The reforms introduced by the Chairman of the USSR Council of Ministers Alexei Kosygin were subjected to criticism by the Party leadership following the "Prague Spring", as it became alarmed at the prospect that economic reforms could lead to political upheavals. The once young senior managers were getting on in years, there was no turnover of personnel at the very top, and new ideas no longer were a source of inspiration but rather apprehension. 					</p>
						<p>
						The Soviet Union was saved from economic collapse by the oil crisis that hit the world in 1973. Oil prices surged, and mega-dollars poured into the USSR. But they were not used judiciously, as it seemed that the oil would last forever, bringing dividend after dividend. The idea of building a just world gradually gave way to the idea of a peaceful, moderately prosperous life. 					</p>
						<p class="blockquote">
						The enthusiasm of the masses can also be short-lived. Youthful romanticism invariably fades away when families and children appear, and a new way of life requires stability and comfort. But too much was built in a hurry. People yearned to return to their homes, the big cities and capitals. 					</p>
						<p>
						In the late 1970s the extraction of Western Siberian oil was seen to be in decline. The fields began to dry up. Salmanov more than anyone understood the reason why the oil yields were suddenly reduced.  In 1987 Farman Salmanov became the First Deputy Minister for Geology of the USSR. Fate played a hand in bringing him together again with Nikolay Baybakov, as the Soviet government instructed them to produce forecasts on the long-term development of the Soviet Union's fuel and energy complex. 					</p>
						<p>
						Veteran oilmen spoke of the critical condition of the oil industry, of the necessity to think about its future, and of the danger of reckless extraction of oil in a country that had an urgent need for energy. Salmanov said that in terms of its oil reserves per head of population the USSR was not among the world's leaders, in fact it was not even in the top ten. 					</p>
						<p>
						However, times had changed, and world oil prices had also. And the country that Farman Salmanov had believed in and and worked hard for disappeared. A new era had arrived, an era of new ideas. 					</p>
