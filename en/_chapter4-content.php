						<h2 class="h2 title js-title" id="power">
						Oil and power
						</h2>
						<p>
						In the end Salmanov was required to report to the Kremlin, and to Nikita Sergeevich Khrushchev himself. Surprisingly, Khrushchev praised the oilmen and castigated Nikolay Baybakov, who was accompanying them as the former "oil" minister and former head of the RSFSR Gosplan.  It turned out that Baybakov, whom Khrushchev had sent to build sugar factories in the south of Russia in the office of the Krasnodar Council of the National Economy, had "missed the boat" on the Western Siberian oil. 					</p>
						<p>
						However, after his short tongue-lashing of Baybakov Khrushchev again had a change of heart and praised Farman Salmanov and his colleagues to the skies. It was particularly important that according to Khrushchev the whole world should know about this discovery, because it was yet another success of Soviet society, and the Party and entire nation could be proud of it. Thus was started the blanket coverage of the oilmens' successes in the Soviet press, and soon afterwards the international media. The "Third Baku" dreamt of by Academician Gubkin, became a reality. 					</p>
						<p>
						Years later Farman Salmanov came to the conclusion that Khrushchev, despite being obsessed with sending a man into space, realized after Protazanov's first report just how important a trump card had suddenly fallen into his hands. A lot of oil offered the solution to a lot of problems that were engulfing the country. Many regions were suffering from shortages of the most basic goods. And if the first Sputnik and the "Thaw" had improved the nation's morale, hard-currency earnings would allow the material issue to be solved. 					</p>
						<p class="blockquote">	Много нефти — это решение целого ряда проблем, которые вовсю наваливались на страну. Во многих регионах были уже проблемы с поставками самого необходимого. И если первый спутник и «оттепель» улучшали моральное состояние народа,  то валютные поступления позволили бы решить и материальную сторону вопроса.
						</p>
						<p>
						The oil of Western Siberia brought to the fore a whole cohort of Party leaders. On the one hand, of course, the Tyumen District Party Committee at first played a very positive part because it was a real force and one with which the scientific, industrial and even Soviet community had to reckon with. The Party earmarked funds to extend the geological exploration and prompted organizational changes; for instance, the Surgut oil exploration expedition was turned over to the supervision of the Tyumen geological trust. But Party bodies demanded the work be accelerated, more oil to be extracted, new records to be set and it all had to be reported to Moscow. It was not long before problems and strategic needs began to be forgotten amid the clamorous, rousing reports. 					</p>
						<figure>
							<img src="img/chapter4/chapter4__6.jpg" class="figure__img">
						</figure>
						<p>
						The authorities now had in their hands a grandiose instrument for the development of the country, and they went full steam ahead with it. For many years Farman Salmanov was a successful and devoted worker in this field, discovering more and more new fields and multiplying by many times the nation's wealth. He would become a Doctor of Geological and Mineral Sciences, Honoured Geologist of the Russian Federation, a Hero of Socialist Labour, Winner of the Lenin Prize, a corresponding member of the Russian Academy of Sciences...					</p>
						<p>
						But that all lay in the future. Right now he needed to open up what the country's natural landscape had to offer. And for that he needed people, lots of people. 					</p>
