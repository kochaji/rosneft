						<p>
During Mendeleev's trip oil was valued for refining, where it was boiled and turned into kerosene, at about 30-40 kopecks per pood (which was commensurate with the price of wheat flour) or, if valuing it by today's standards, approximately from 2 roubles 60 kopecks to 3 roubles 40 kopecks a barrel. That said, the price of kerosene in the central part of Russia rose to 4 roubles per pood — in other words, the finished product cost 10 times as much. 						</p>
						<p>
It is true that at the time the word 'kerosene' was not known in Russia. It was a trade mark in Britain, where the liquid for illumination lamps in Russia was given a very pretty name — 'photogene', which in translation means 'light-producing'.						</p>
						<p>
Given that, kerosene lamps of that time when burning used up about 50 grammes per hour, a kilogramme of kerosene sufficed for approximately 20 hours, that is, one hour was a little more expensive than 1 kopeck. A kopeck, however, back then in Russian markets was worth quite a lot 'in weight'. 						</p>
						<p>
It was clear that any profit from kerosene production was essentially eroded through the number of middle-men and third-party participants of this archaic technological chain, including manufacturers of timber for the barrels, coopers, and specialists in cask stitching. 						</p>
					</div>
				</div>

				<div class="row">
					<p class="blockquote col-sm-offset-5 col-sm-12">
It is difficult to describe the scale of underhandedness of the business in Baku 150 years ago. All participants of the production process the authorities and supervisory bodies were simply intertwined in an infernal morass that had been largely brought about by the system of buy-backs which at that time determined how the oil fields were distributed. In and around Baku a number of small enterprises operated which engaged in vicious competition with each other, with competitors' wells and refineries set on fire, potential newcomers were 'squeezed out' as a matter of course, and there were kickbacks to administrators. The most powerful force was the army of more than10 thousand carriers of the oil, organized by powerful local forces, to all intents and purposes a transport mafia. Defeating it seemed impossible. 					</p>
