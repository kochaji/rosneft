The fact that the Dutchman had good reason to flee Baku, notwithstanding the seeming profitability of the oil business, became clear to Robert Emmanuelovich very quickly. Either the Nobels had connections at the very top, including the Sovereign himself, or complaints against the oil buy-backers had reached the most entrenched government civil servants, but in 1872 the system of buy-backs was abolished and the oilfields were privatized. In 1873 the first auction took place. Ownership was, however, encumbered by a special tax — 10 roubles per decima (1.1 hectares) every year and indefinitely.						</p>
						<p>
Robert was able to modernize his factory and in1875 put on sale 300 barrels of good kerosene. It was then that his brother Ludwig took an interest in the business, not only a talented engineer, like Robert, but also a businessman of some acumen, and he began to analyze the whole technological chain of production, beginning with oil extraction, its transportation to oil refining facilities, the refining process, transportation to central Russia, distribution, and advertising the product. The Nobels began actively studying the American experience, and it was here that Vladimir Bary and Vladimir Shukhov crossed their paths — young, imposing and already successful engineers. 						</p>
