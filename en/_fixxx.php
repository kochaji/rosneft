Shukhov was a real engineer of the European school. He had a great command of mathematics, with a sound knowledge of physics and chemistry, and was able to converse in three languages fluently, he was a keen student of the contemporary professional literature, and he understood the complexity of the processes with the pipeline and inside the pipeline. And, incidentally, around the pipeline also. He not only designed the pipeline, Shukhov also designed the business. Like his intellectual 'guru' Mendeleev, Vladimir Shukhov saw in science a tool for entrepreneurship.					</p>
					<p>
Shukhov considered the pipeline as a system consisting of two stations: one transmitting, the other receiving. In order to pump oil along the pipeline, pressure had to be created in the transmitting station. But only thick pipes could withstand high pressure. Such pipes were more expensive, they ruptured more frequently and assembling the pipeline became more expensive. In other words, the system threatened to become uneconomical. There were several possible solutions: to have additional pumping stations or to increase the temperature of the oil being pumped. Or the fuel oil. 					</p>
					<p>
Fuel oil at the time was considered not worthy of attention. Fuel oil was what remained of the refining and release of kerosene cut. A thick mass, thicker than oil. But the more Shukhov worked with oil, the tougher it was for him to see that only part of it became the actual product. Fuel oil also had to be used to bring in a profit. For instance, as fuel for the steam pumps which pumped the oil along the pipelines, for any other steam utilities, including the steam ships that carried the prepared kerosene across the Caspian Sea to the ports of the Volga. 					</p>
					<p>
The young Shukhov had once designed a steam atomizer for the burning of fuel oil. Ludwig Nobel bought the patent for it from Shukhov for use in his refineries. So a fuel oil pipeline now had to be built. And Vladimir Georgievich again sat down to his calculations. He already knew that the viscosity of fuel oil was reduced at higher temperatures so it was easier to be pumped. He deduced the dependence of the pipes' diameter, the temperature and the amount of fuel oil used. For extra economy he proposed heating the fuel oil with steam from the steam pumps, which pumped this very fuel oil. 					</p>
