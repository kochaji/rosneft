						<h2 class="h2 title title--start js-title" id="years13">
							13 years later...
						</h2>
						<p>
Nowadays it is common to think, that freedom of behaviour and choice of employment guarantee the emergence of intellectual giants. But our forebears assumed, that only discipline and organization could manufacture a worthy citizen and valued expert out of a loud-mouthed schoolboy. 						</p>
						<p>
For this reason the Imperal Moscow School of Engineering was distinguished by its particularly harsh regimen — arguably, those in army barracks today have it cushier. The professors believed, that if a student was forbidden form doing almost anything apart from study, he would dive in to it head first, so as not to go out of his mind. This was the seat of higher learning Vladimir Shukhov entered in 1871, after graduating from grammar school. 						</p>

						<div class="blockfloat blockfloat--right iblock">
							<img src="img/young-shuhov.jpg" alt="Шухов" class="inline-img">
						</div>

						<p>
Vladimir grew up in a family of modest means, and at that time the career of an engineer was a real possibility of achieving success in life, as engineers were in great demand. The country was experiencing industrialization, and entrepreneurs, wishing to invest capital, were seeking out professionals. Volodya Shukhov, meanwhile, was a very promising student. While still in his third, or second year he devised an atomizer, in other words a spray that would burn up heavy liquid oil: what we know as fuel oil. 						</p>
						<p>
It is no surprise, that Shukhov took an interest in oil and everything that was connected with it. By that time Dmitry Mendeleev had discovered the Periodic Table several years earlier, and his active stance in defending the further and extended uses of oil, naturally, was supported by the student body. Their hero had not yet turned 40, and students saw in him an older companion who would lead them forward out of the stagnant world of old science and a blinkered life . 						</p>
					</div>
				</div>
				<div class="row">
					<p class="blockquote col-sm-offset-5 col-sm-12">
They were particularly incensed, by the bureaucratic red tape that could put a stop to interesting projects. They looked on bitterly, as ideas, expressed by their Guru, were implemented abroad, in the USA. Mendeleev himself was not best pleased. The time of the Russian breakthrough had not yet come. What was needed was an idea, a push. 					</p>
				</div>
				<div class="row">
					<div class="col-sm-offset-5 col-sm-12">
						<p>
The time came to leave behind the walls of his alma mater and Vladimir Shukhov received a lavish proposal from Pafnuty Chebyshev, a mathematician with an international reputation : to work together. A twist of fate indeed: in 1863 Mendeleev rejected practical work in favour of science, and in 1876 Shukhov rejected academe to take up practical work. 						</p>
						<p>
The question was: how could a talent that wished to leave the ranks of scholars be further encouraged? Or just as well, perhaps, make him change his mind. The School Council decided that a trip to America for a young and up-and-coming engineer would be just the job. Especially as it was not only a trip, but an official assignment to visit the World Exhibition. And who would turn down the opportunity of seeing the world...						</p>

						<h2 class="h2 title js-title title--divider" id="friends">
							Friends and competitors
						</h2>
						<p>
By this time 11 years, had passed since the last salvoes of the American Civil War had died down. At the time Russia's position was one of benevolent neutrality, not recognizing the separatists of the Confederacy, which rather astounded the rest of the world as logic would dictate that land-owning Russia would support the plantation owners of the South. Chancellor Gorchakov, however, believed that the USA was the main trading competitor of Great Britain, and in the general scheme of things Europe, and that it was in the interests of Russia to have the USA as a partner, and not an enemy. 						</p>
						<p>
There was a special relationship between Russia and the USA because of the sale of Alaska. Russia's logic was simple: to maintain this territory on the borders of the British Empire (to wit, Canada) was in practical terms impossible. And the USA with its territory blocked British access to Chukotka and Kamchatka. This was decades before there was any Atlantic solidarity. There was a Pacific solidarity, however.
						</p>
						<p>
Public opinion in the USA, thereby, was wholly on the side of Russia. This explains the reception afforded to the Russian delegation that arrived for the World Exhibition, dedicated to the 100th anniversary of American independence (which, incidentally, at the time was supported by Catherine the Great through 'armed neutrality'). The delegation was promised it would be shown everything that was of interest, and would be helped in purchasing high technology equipment. One of Shukhov's interests was the experience of building oil pipelines. 						</p>
						<p>
The Russian visitors to the USA had a friend — Alexander Bary — also a remarkable individual. He was the descendant of French immigrants who had fled to the Russian Empire from revolutionary France, but through an irony of fate Alexander Bary's father himself flirted with Marxism, and after, he became the focus of interest of the Tsarist police in 1863 , deemed it prudent to leave for the USA. Therefore Alexander, born in St Petersburg, educated in Zurich, moved to be with his family in the USA, where he took American citizenship. This, however, did not prevent him from being a Russian patriot and help his friends in their mission. 						</p>
					</div>
				</div>
