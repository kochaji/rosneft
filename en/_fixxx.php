It should be said that the American experience could also be a hindrance. The secret was simple: the oil pipeline business in the USA was started up by dynamic but poorly educated entrepreneurs. There was a famous incident, when during the winter a pipeline ruptured that had been laid in the summer, and  then with the onset of the summer heat a 'winter' pipeline buckled and snaked out and along a road, destroying neighbouring houses simply because the builders had not taken into account the fact that metal expands at different temperatures. 					</p>
