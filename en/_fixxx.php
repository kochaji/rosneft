It wasn't only transportation problems that had to be solved but also issues of storing the oil and its products. In Baku oil waste, in other words fuel oil, was poured into pits, and the finished product was kept in barrels or strong rectangular tanks on a thick, solid base. But Shukhov was a mathematician, and in his view a solid base was ungainly and unpractical. And expensive. It was not needed, what was needed was a platform filled with sand, with a flexible steel sheet placed on it and a concrete ring along the sides. Indeed, the walls would also be made of steel rings, and the higher they were, the thinner they were. This was because the pressure of the liquid in the upper part is always lower than at the base. This idea was like a bolt of lightning. It was not long before everyone started ordering new oil storage facilities from Bary's engineering company. Up to 1917 Bary's firm would build 20 thousands of similar reservoirs. Their design would set the international standard. In the film 'White Sun of the Desert' comrade Sukhov would hide Abdullah's wives in just such a reservoir. Even today oil storage facilities are made according to this principle.					</p>
