							Nobel enters the fray
						</h2>
						<p>
There is a story, that in 1870 a mechanics factory in St Petersburg belonging to the Swedish-Russian family Nobel received a new order to manufacture weaponry. This was something rather routine for the Nobels — back in 1842 Emmanuel Nobel came to Russia and began the production of weaponry and naval mines. His sons, Ludwig and Robert continued their father's work but leaned more towards mechanics, just as Alfred, the future founder of the Nobel Prize, leaned more towards chemistry, with the creation of new types of explosives. 						</p>
						<p>
Thus, Robert Nobel travelled to the south of Russia to purchase walnut-tree wood for the manufacture of rifle stocks at the family's factory. Along the way he made the acquaintance of a Dutchman, who produced oil on a rather small scale in Baku. As he listened, enthralled, to the Dutchman's story, Robert decided to get involved in this new, and promising business, seeing larger possibilities there than the compatriot of Till Eulenspiegel could even imagine, who himself had by now grown tired of Oriental exotica and was glad to sell Robert his small refinery.						</p>
