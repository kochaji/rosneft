					<div class="col-sm-offset-5 col-sm-12">
						<p>
Bary's life in the USA was a good one — his work was with construction metals at the Exhibition earned him a gold medal as reward. So this man arranged for the Russian delegation not only a schedule of equipment purchases, but also special excursions to refineries and pipelines. Shukhov saw what could be developed in Russia, in Baku, and also what could not appear soon enough. But at the same time he identified some loopholes in the decisions made by the Americans... He would correct them, but not just now. Because order is order, and after this wonderful trip it was time to do the work he had been assigned to do. But a dream had emerged, which, moreover, began to acquire a definite form. 						</p>
						<h2 class="h2 title js-title title--divider" id="regress">
						Awareness of anti-progress
						</h2>
						<p>
The road to one's dream is never straight. On returning from the USA in 1877 the gold medalist was assigned to the draughtsman's office of the Warsaw-Vienna railway line. A routine of necessary but routine projects began — bridge crossings, water supply stations, station buildings and separate toilet facilities. A Shukhov family friend, the famous surgeon Pirogov, heard about the young Vladimir's frustrations, and advised him to busy himself with something more engaging — for a start, to go and attend a surgery course in the Military-Medical Academy.
						</p>
						<p>
Bary's arrival saved Shukhov from routine or surgery — together with his family he returned to Russia. The passport of a citizen of the United States protected him from any possible security issues, and he was able to give himself over entirely to business. Bary realized that Russia was only at the very start of its industrial development, and here he could, using American experience and methods, make progress very quickly. The commodity with the greatest potential at that time was oil. The Baku oilfields, however, were in a surprisingly archaic condition. 						</p>
						<p>
It is a myth that oilmen must always support progress. Big business is a system of agreements, many of which are calculated for decades in advance, loans and connections. In the short term breaking something up does not always look to be profitable. Thus, oil extraction on the Apsheron peninsula was carried out in the most primitive way: from wells or shallow drillholes, using the most labour-intensive methods. The oil was then poured into casks, loaded on to horses and taken to the coast, where it was evaporated into kerosene. Two thirds were poured away into huge lakes. 						</p>
					</div>
				</div>
