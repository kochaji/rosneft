						<p>
The young men acted fast. Alexander Bary in 1877 opened in Baku a branch of his construction firm, whose chief engineer was Vladimir Shukhov. Bary and Shukhov assessed the situation quickly and realised that here was an untapped source of work, where they could apply the skills they had learned in the USA.  But knowing Russia well, they realized that you can't chop wood with a penknife, and they would have to be circumspect, securing support from influential forces. 						</p>
						<p>
Ludwig Nobel immediately saw the significance of the proposal of Bary and Shukhov to replace barrels with a pipeline. References to the American experience also helped, as did those to Mendeleev's authority, whose name was by now known throughout the world. Bary and Nobel drew up a contract to construct a pipeline from the Baku oilfields to Nobel's factory in Black City with a throughput capacity of 50 tons per hour, and engineer Shukhov, just turned 25, obtained total freedom for engineering decisions.						</p>
						<p class="blockquote">
But having freedom for engineering decisions and freedom of action are two very different things. The idea of laying down a pipeline immediately ran into opposition from the authorities. And not because they were against technological progress per se, or were disconcerted by the American citizenship of Mr Bary. They feared a social explosion, and possibly the collapse of all the networks of corrupt relationships that had been established. 						</p>
						<p>
It did not take a genius to realise that a pipeline would make thousands of oil carriers redundant. And it would be not only the carriers who would suffer, but also those who controlled the business, and had close ties with the local administration. The mooted changes were greeted with apprehension by the public, and both the public and the authorities were alarmed at the prospect of mass disturbances, unemployment and even ethnically-grounded conflicts. Things came to a head with a threat to forbid laying a pipeline that was situated on state property. It was clear that what was at stake was not only the construction technology, but also the technology of promoting innovation, in other words marketing. 						</p>
						<h2 class="h2 title title--divider js-title" id="pipes">
							Pipes and people
						</h2>
						<p>
Shukhov wasted no time. As a matter of course he could tackle several issues at the same time, and could just about do everything himself, whether it be examining the finished product, or getting his hands dirty at the manufacturing lathe. He wasted no time in ordering pipes for the pipeline in the USA, the cheapest but also the best quality available. Welding had yet to be invented, so the pipes were connected by threaded joints aided by sockets. 						</p>
						<p>
The length of the pipeline with the technical releases was little more than 12 kilometres, the diameter of the pipe was three inches, which is to say 76 mm. In other words the pipe itself was considerably shorter than the longest American pipes. Yet this first pipeline was being built by a young man in whose idea very few people in Baku had much confidence. 						</p>
						<img src="img/pipes.jpg" alt="" class="inline-img col-sm-20">
						<p>
Attitudes towards him were not even scepitcal, but outright hostile. This young man had the temerity to break down established traditions! The circle of the disaffected was getting larger all the time: the carriers were joined in their opposition by the owners of coopers' businesses who manufactured barrels, and armator offices, all of them fervently hoping that Shukhov's project would founder.						</p>
						<p>
Even Ludwig Nobel, who handled the business aspects, was uncomfortable. Nobel initially invited other oil entrepreneurs to invest in the business by buying shares, as their profit margins were being hit by the tactics of the carriers. But his colleagues and competitors simply took fright at public opinion, and the possible consequences for them personally. Rumour has it that other entrepreneurs simply said: 'You Swedes can leave at any time for St Petersburg, or your homeland...Our homeis here, we have to live here, with these people...'						</p>
						<p class="blockquote">
As a result Nobel began financing the pipeline on his own. No sooner had construction begun than the warehouse where parts imported from America were stored burned down. The pipes that had already been laid were drilled at night, and connecting points were covered with acid. Nobel was beside himself, but was not prepared to turn his back on the project. 						</p>

						<p>
Shukhov noted that among the carriers there were those who wanted something better in their lives than pulling barrels from the fields to the factory. They had been hired on good wages to carry the construction materials of other workers, and they got clean drinking water and foodstuffs. Nobel paid more, and qualified workers were among the elite of the working class. Nobel hired Cossacks to protect the pipeline, and set up watchposts and towers along the pipeline's route.  						</p>
						<p>
While Ludwig Nobel organized the work on the pipeline, Vladimir Shukhov completed its construction in only a few months. The year 1878 entered the annals of the history of the Russian oil industry. The Nobels' colleagues and competitors were amazed, and realized what an opportunity they had missed by not investing in such a profitable enterprise. 						</p>
						<p>
But the Nobels to everyone's surprise announced that they would pump the oil of all who so desired. For only five kopecks per pood (instead of 20 kopecks for it to be transported by the carriers was an excellent saving). The next year the Lianozov oil company placed an order with Bary's firm to build just such a pipeline, but several kilometres longer. Another 10 years later near Baku there would be 25 pipelines totalling around 300 km in length, transportation costs would drop to a mere 2 kopecks per pood, cheaper than the cost of extracting the oil. But Shukhov did not build these new pipelines, because after building the first ones he became involved in other aspects of the business, entering hitherto unexplored territory where his American experience was of no help to him...						</p>
