The future oil production pioneer Ivan Mikhailovich Gubkin would have had to master the dead-end trade of a barge-hauler had it not been for his grandmother Feodosya Nikiforovna, who nurtured the most progressive views on young people's education. She is believed to have sewn him the schoolbag herself, along with notebooks that she crafted out of paper trimmings. His father gave in, hoping the skill to read and write and count would prove to be useful once his son will have mastered them. 						</p>
