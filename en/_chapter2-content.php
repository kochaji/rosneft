								The skill to take the blow
							</h2>
							<p>
With learning going quite easily, Ivan rather enjoyed school. He soon proved to be what we now call an A-student and, consequently, became the object of mockery and envy of his fellow-students. He was taunted and called names, but Ivan never lacked physical strength or perseverance and didn't let anybody keep him down. This calm determination seems to be the Gubkins' family trait - one can hardly expect a choleric tempered man with a feeble physique to be able to haul barges. A dislike for envious people, remarkable perfectionism and the striving urge to achieve at whatever cost would turn to be crucial in his life in the years to come. 							</p>
							<p>
In the meantime Ivan's father harbored plans for his clever son. He saw his heir on the position of a clerk or even a counterman in a shop. That seemed a career peak for a barge-hauler, especially with no point in pursuing a career in the barge-hauling guild when progress slowly but surely was pressing bargemen from the towing business. Ivan's far-sighted father understood well enough that if there would still be enough work for him to do, his son was a different matter and would have to look for a job elsewhere. 							</p>
							<p>
Meanwhile Ivan was enjoying learning immensely. Fortune smiled on him on the day when the public school inspector took notice of his grades. The young man was clearly talented. Ivan got a chance to continue his studies in Murom district college. His father might have objected, but Ivan was lucky to have a younger brother who had to take over their father's profession. 							</p>
							<p>
Studying in the college went on full scale and with a great deal of success too. Then came the moment to make a fateful decision -  whether to take advantage of the demand in which educated young men were held and go to work. He could claim a "clean" position of a clerk in a number of enterprises that would have settled him well-off. But Ivan decided to continue his education in Kirzhach normal school. 							</p>

							<p>
Education cost money. And he agrees to a deal - a stipend of 6 rubles 57 kopecks (enough to live on but with limited comfort) would be paid in exchange for his obligation to work for five years as a village teacher by appointment in his home county. Did he want to become a teacher? Very unlikely. He regarded it rather as a step towards the future. 							</p>
							<p>
A very significant story happened to Ivan at this time. He wrote an epigram on his schoolmate, a remarkably inspired tattler. Such freethinking was not appreciated back then and Gubkin found himself on the verge of expulsion. The matter was settled with the help of his friends who testified in his favour and provided moral support, and not just that. There is evidence that when Ivan lost his stipend they refund it from their own money. 							</p>
							<p>
Eventually, in 1890 he becomes a local teacher and is sent to work to Karacharovo village, the birthplace of the famous Russian bogatyr Ilya Muromets. Teachers were highly ranked those days and Ivan Gubkin might have made a very different career. But things turned out otherwise after he met the local priest. 							</p>
