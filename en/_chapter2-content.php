Gubkin used to say "earth depths won't let down, if people don't". As years went by, he grew more sceptical of the quality of human resources. It seemed to him that people don't want to work, to strive and in this they sabotage the cause. He judged everyone by his own standard but unfortunately few could stand up to his scale. That was when Gubkin began to complain, writing about saboteurs, demanding to deal with complacency. His letters came to use when the repression period began in the USSR. This part of his biography is still very controversial and divides opinions.				</p>
