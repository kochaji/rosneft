On 22 September 1942 the State Defense Committee has decreed to speed up exploration works in Povolzhye and Transurals. Over ten thousand specialists along with the best drilling devices have been transferred from Baku under the danger of the German forces' possible breakthrough. Productive oil-pool has been extracted in the Vereiskian oil-field in 1943. Gubkin's theory proved to be right and now the Soviet Union had a real "second Baku". 					</p>
