His concern proved to be true. In 1940, when Great Britain was at war with Nazi Germany, whom the USSR supplied with crude production, as per the Molotov-Ribbentrop pact, the British headquaters harboured an idea to strike a blow on the Bakuvian oil-fields, so that the USSR would cease sending gasoline to Germany. 					</p>
