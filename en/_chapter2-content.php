Mikhail's prayers have been answered and in September 1871 he had a son, whom they named Ivan. He grew up to be a lively and inquisitive boy and as the years past, he closed on the age when children were sent to school. His father believed it to be a whim. Being illiterate, knowing only the basics of count, he nevertheless managed to make his way to be a senior. Ivan himself, curious as he was, was not eager to go to school. It wasn't common practice either, one was thought to do well without studying. 						</p>
