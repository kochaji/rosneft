Despite being a peasant, Mikhail Gubkin didn't earn his living by farming, but worked as a senior in a barge-haulers guild. He hauled barges along the Volga and its tributaries throughout the whole season and when frosts settled in, returned home to the village of Pozdnyakovo, some ten miles east of Murom on the bank of the Oka. 						</p>
