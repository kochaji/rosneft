Gubkin knew all too well the significance of oil in the First World War and the Civil War in Russia. In the 1930s, that was his motive for promoting oil development in Ukhta and Povolzhye, the reason was that one could not afford to be too dependent on oil fields located within a strike proximity of the enemy's bomber aircraft. By which he meant Baku in the first place. 					</p>
