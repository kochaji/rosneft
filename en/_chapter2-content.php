At this point Gubkin is not only an academician, but a living symbol of the Soviet oil geology. On 17 April 1930 Moscow Oil and Gas Institute was named after Ivan Gubkin. Not every scientist can boast such an honour. But his experience as a village teacher couldn't have been for nothing and he was a truly excellent professor that could speak of complex geological matters in simple language, equally comprehensible for students and members of government. He had a gift for gaining people over to his cause. 				</p>
