Ivan Gubkin was undoubtedly a true patriot of Russia, at any regime. He believed that in the 20th century oil was no longer simply a mineral wealth but also a global factor of world politics. Gubkin was familiar with the famous report that the French senator Henry Brenger presented to Georges Clemenceau, Chairman of the Council of ministers of France in 1919, a report that had the following words: "Take hold of oil - take hold of power". Oil extraction was therefore a matter of state. 				</p>
