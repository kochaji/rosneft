					A Route to the Future
					</h2>
					<p>
					After the government committee signed the pipeline acceptance and operation commencement certificate on June 19, 1942, the attitude to pipeline transportation changed dramatically. On the very first day of operation petrol of high quality went down the pipeline. More than 17 tons of highly-valued fuel were transferred to the receiving terminal at Borisova Griva every hour. 					</p>
