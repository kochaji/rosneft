					<h2 class="h2 title title--start js-title" id="waves">
					Above and Under the Waves
					</h2>
					<p>
					After this loss, a decision was made never to work in a storm under any circumstances. The importance of divers grew - they constantly examined the bottom where the pipes were laid. Besides, they were delegated the task of fixing the pipes on the bottom so as they would be stable enough not to be carried away by the current. Cast iron loads weighing 50 kilograms were hung on the pipes. They were hung every fifty metres at a depth of less than four metres, and half as frequently again at a greater depth. 					</p>
					<p>
					In shallow waters and the coastal area the pipes were buried with the help of a jet gun breaking the sandy and muddy bottom. Thus both ends of the pipeline were made inconspicuous against the background of the traditional crossings which were still functioning. Camouflage and active lake navigation allowed for keeping the pipeline works secret. 					</p>
					<p>
					Stalk laying was started on May 31. The EPRON men worked almost ceaselessly. The engineer diver Sokolova did a considerable amount of dives together with her colleagues. The welders who worked on the shore increased the speed of work - they could already weld three sections per day, that is, almost one kilometre. In two weeks, on June 14, the last stalk, the fifteenth, was welded to the pipeline. The pipeline was laid across Lake Ladoga. 					</p>
					<p>
					But this didn't mean the end of the work. The divers once again checked the pipeline all the way along its length. The bend radii conformed to all of the project parameters. There was no sagging. Then the tests began - they started with piping in some water. After that they pumped in kerosene, gradually increasing the pressure. Finally they injected kerosene in the pipeline under pressure of more than 20 atm and started watching the manometers. The divers checked the joints trying to find some points of leakage but there were none. Three days later, on June 18, the pipeline was ready to be put into operation. 					</p>
