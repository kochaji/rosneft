					<h2 class="h2 title js-title" id="front">
					The Pipes From the Fighting Line
					</h2>
					<p>
					From the very moment of taking the decision to build the "OS - 6" facility, the executives in charge contacted the directors of industrial plants and asked them one and the same question: "Do you have any pipes?" They found out that 100 mm diameter pipes made for oil wells were kept in the warehouse of the Izhor plant in Kolpino. There was only one problem - the fighting line ran through the area. 					</p>
					<p>
					Maybe it was because the warehouse with the pipes was very close to the German positions that it was preserved in a relatively good condition. Schinberg went there himself to select the pipes for further transportation to the building site. 					</p>
					<p class="blockquote">
					It turned out that the words "warehouse at the front line" were to be taken at face value. Schinberg with his convoy had hardly started measuring the pipes when an artillery attack began. The warehouse keeper who was in the convoy died. The new pipes were twisted by the force of the explosion and damaged by the shell fragments right in front of their eyes. It was clear that they had to work very gently and only at night.					</p>
					<p>
					On the other hand the pipes were of excellent quality. The thread was protected by special caps, the pipes were marked according to the steel grade and already were base coated. It was simply impossible to lose such a treasure and the pipes were literally carried out by hand. 					</p>
					<p>
					Eventually, the builders of the pipeline had two sorts of pipes. About two thirds of the pipes had no thread, and almost one third of them had a thread and a sleeve joint. A decision was made to weld all the pipes for safety, including those which were first connected by joints. Such connections were to be made between longer stalks at first, welding them in water. 					</p>
					<p>
					As the builders had few high-quality pipes, they decided to transfer the eastern entrance of the route to a protruding land tongue on the eastern bank. They managed to save a few kilometres of the underwater section as a result. The pipes had to be buried at a depth of up to 13 metres. 					</p>
					<p>
					Some "tables" were prepared at the eastern bank near the Kokorevo settlement, where pipes of 5, 6 or, very rarely, 7 metres long were welded together into sections 300 metres long. And the question there was: how good will the welding be? Engineers could do nothing - it was all in the hands of the welders. 					</p>
