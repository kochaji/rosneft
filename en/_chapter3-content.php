					<p>
					When the war was over many former EPRON professionals committed themselves forever to building underwater pipelines and oil and gas platforms. A specialised administration for building trunk line underwater passages known today as "Podvodtruboprovodstroi Open Joint Stock Company" was created in 1956. Experts from this company built underwater passages across the biggest rivers of Asia, Europe and Africa, laid underwater pipelines on the shelves of the Baltic and the Black Sea, the Caspian, the Mediterranean and the Sea of Okhotsk. 					</p>
					<p>
					Still, it is the Ladoga pipeline that stands out. According to the builders' recollections, the secret of success was that all of them - field engineers, design engineers, divers, welders and tow boat operators - understood perfectly the essence of the given task, organised the production process on their own site, looking forward for the demands that may occur at the next technological stage. The trust which the project directors put into the directors of separate sections and, in their turn, into the actual performers of the work, allowed for the waiving of red tape delays. 					</p>
					<p>
					The story of building the Ladoga pipeline is a story of brilliantly organised production where military supply services were involved alongside of the units of the Navy (EPRON) and civic design organisations. They managed to wade through departmental barriers, create a new organisation and fulfil a most difficult task with excellent quality and ahead of schedule. 					</p>
					<p>
					As it happens, these people wouldn't meet the definition of "heroes" in the Soviet era. But as the time goes by, it becomes clear that they possessed what was needed back then and what we lack nowadays - the skill to do one's job in a clear, creative and effective manner - and with minimal losses. In a word, professionally.   					</p>
					<p>
						No one would be amazed by the pipeline itself these days. But the case of successful organisation and management is worth a careful examination and builders themselves should be honoured and well remembered. As every long-distance pipeline that negotiates water barriers or is hidden under the waves of rough seas is the descendant of that very pipeline. </p> 					<p class="strange-square text-center">◼</p>
