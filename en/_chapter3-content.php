					<h2 class="h2 title js-title " id="nofuel">
					When there is no fuel left...
					</h2>
					<p>
					The last train left Leningrad at the end of August, 1941. German forces cut off the railway line and were heading north to Lake Ladoga. On September 8, the Wehrmacht reached Shlisselburg, the place where the Neva flows from Lake Ladoga. Finnish forces successfully made their way north of Ladoga closing the ring of the siege. On September 10, the Badaev warehouses with large quantities of supplies were bombed. 					</p>
					<p>
					In September, Hitler with plans to hasten the fall of Moscow in mind, gave the command to call back some tank units from Leningrad, to intensify artillery and air strikes on the city and to begin its systematic destruction. There were lots of refugees from the occupied territories in the city at the time, mainly old men, women and children. They could do nothing to protect the city and were most probably constituted a burden. There was a need to arrange the evacuation of the civilian population from Leningrad so as to improve the supplies for the remaining defenders of the city. Leningrad was running out of provisions and fuel - fuel for military equipment in particular. 					</p>
					<p class="blockquote">
					Leningrad had become an "island" which could be supplied only via Lake Ladoga. But autumn came with the season of Ladoga storms, and the thirty kilometres of water between the eastern and western banks of the lake had to be crossed not only under the enemy's fire but also in the toughest weather conditions. 					</p>
					<p>
					Even Peter the Great had faced the problem of autumn and spring storms on Ladoga, during which the waves, steep and choppy, rose up to 5 metres and blocked vessels with cargoes for building St. Petersburg. Up to a thousand ships of different sizes vanished every year, and due to this fact Peter gave orders to build a canal more than 100 km long along the south coast of Ladoga in 1718. A new canal was built under the reign of Alexander II. This canal is still in use today. 					</p>
					<p>
					The fleet the Soviet side had was too small. Moreover, the enemy successfully sank barges containing people and cargoes. But the winter which was closing in fast brought hope of an ice motor road. Indeed, the "Road of Life" was opened on November 28, 1941. 					</p>
					<p>
					It was down this road that fuel was planned to be transported to besieged Leningrad. But the calculations made by the supply officers were disappointing: more than seventy thousand tons of fuel and lubricants were to be brought in. According to the calculations, the fuel from seven thousand 10-ton railway tanker cars had to be pumped to here. 					</p>
					<p>
					25 thousand tanker lorry trips had to be made to carry this amount of fuel, and even then the lorries had to go loaded only in one direction, and come back empty. It would have been more useful to carry fuel in barrels on lorries so as to evacuate people in the barrels on the way back. But hundreds of thousands of iron barrels were needed for this. The return of the empty barrels wasn't part of the calculation. And even if they had the barrels available, they didn't have enough lorries. So the usual approaches couldn't provide a comprehensive solution to the fuel problem. There was a need for another fresh idea. 					</p>
