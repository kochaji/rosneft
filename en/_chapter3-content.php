					<h2 class="h2 title title--start js-title" id="project">
					A Project Which Did Not Exist
					</h2>
					<p>
					To be in charge of such an important and unprepared project means to be held up at gunpoint. There is a huge risk of failure, its consequences will be terribly harsh, and the punishment, taking into account the peculiarities of wartime, can be the severest possible. Schinberg only had old calculations, rough layouts and general plans. He also had the crazy responsibility, particularly to the people who had to bring his plans to life. There also was a deadline - June 20, 1942. 					</p>
					<p>
					At the end of April, Schinberg had the following: the pipeline transmission capacity had to be about 500 tons of petrol per day. To this end, he needed from 35 to 40 km of pipes 100 mm in diameter, transfer pumps and containers to hold the fuel along both banks of Ladoga. 					</p>
					<p>
					There were no pipes at all, and he already had to work out the technology of pipeline construction. The experience Shinberg had with the 1934 project came in handy: 					</p>
					<p class="blockquote">
					the pipes had to be connected in sections and coated with a waterproof layer at first, then assembled in stalks and, as soon as the weather allowed, laid on the bottom of Lake Ladoga. The work had to be carried out most inconspicuously, so that the enemy would understand what was being done on the bank of the lake as late as possible. 					</p>
					<p>
					Camouflage was facilitated by the fact that the port facilities and transfer points of "The Road of Life" were situated there. The enemy knew about the constant activity on the banks of Ladoga and tried to deliver air strikes, but air defence was quite strong in this area. Thus the work had to be designed so as not to stand out against the general background.   					</p>
					<p>
					The experts from EPRON, who arrived almost at the same time as Shinberg's team, immediately established the exact underwater route of the pipeline. They suggested building at once two racks for the pipes to be taken down to the water. The racks were essentially roller-type tracks 300 m long. Simultaneously, a team of geological engineers was moving along the breaking ice. They regularly made holes in the ice and collected samples of the bottom soil, taking measurements of the depth at the same time. 					</p>
					<p>
					The lake bottom profile was being formed this way, and later, it was given to the divers from EPRON for drafting further work plans. Thus every unit was itself doing what was needed for the common goal. But they also needed the pipes and the technology of connecting them with each other. And the pipes were kept at the fighting line. 					</p>
