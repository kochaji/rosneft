						<p class="first-paragraph">
						Sometimes an invention or a feat has a name. But the feat we would like to talk about has a lot of names, and we are going to reveal only a few of them, though we will remember that it was a deed carried out by so many people who can fairly be called true heroes and patriots. Having built the first long-distance underwater pipeline in the USSR, they helped the people of Leningrad to survive the Siege. 						</p>
						<!-- ... -->
						<p>
						The story of building the pipeline which carried fuel straight to the fighting and besieged Leningrad is interesting not only as another act of valour of the Soviet people who stood their ground in a deadly fight against a ruthless and powerful enemy. It also is the most important point in the history of the Soviet oil industry, a unique event which triggered a change in attitude toward pipeline transportation in the Soviet Union. And finally - it is a sort of a technological breakthrough.						</p>
						<p>
						In fact there was a time when the advantages of pipelines even over the railway transportation were called into question. Advocates of carriage by rail had been having it their own way for a long time. There also were some problems with technology so the pipes had to be purchased abroad. This all began to change in the mid-1920s when Soviet industry grew more and more oil-consuming. It became clear at a certain point that railway men could no longer cope with the growing scope of transport operations. 						</p>
						<p>
						Oil industry workers prepared a report for the Soviet government in 1926. They highlighted in this report that if the building of the Grozny-Tuapse and Baku-Batumi oil pipelines hadn't been supported in due time, a transportation standstill on the Transcaucasus railways would have occurred. The arguments were significant and the Grozny-Tuapse oil pipeline was put into operation in 1928. 						</p>
						<p>
						And in 1931 the first pipes of the Armavir-Trudovaya pipeline were laid. The pipeline was more than 450 km long. It was put into operation a year later. The building of the most powerful pipeline in Europe - Guryev-Orsk (more than 700 km long) - was started that same year. It was also remarkable due to the fact that it was built entirely from homemade pipes. 						</p>
						<p>
						The year 1932 was marked by the discovery of the Ishimbay oil deposits which became the first fruit of the vast oil-producing province later named "The Second Baku". Initially, it was considered enough to build a railway line from Ishimbay to the oil refinery in Ufa. It became clear very soon that the line at 170 km long couldn't cope with the flow of oil and the decision to build an oil pipeline buried in the ground was made. The pipeline had to be buried to reduce the impact of winter frost on the oil flowing in the pipes. 						</p>
						<p>
						Even the almighty Lazar Kaganovich who promoted railcars for the transportation of oil when he was People's Commissar for Transportation had to admit in 1939 that the reliance on tanker cars was a mistake. But did anyone suspect at the time that there would be so much oil? Only Gubkin believed in "The Second Baku"...						</p>
						<p>
						So in 1931 when David Shinberg, a young graduate of the Oil Institute, started his career, pipeline transportation in the USSR was still in its infancy. And so many solutions had to be sought for. 						</p>
