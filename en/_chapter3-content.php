					<h2 class="h2 title js-title " id="idea">
					The Birth of an Idea
					</h2>
					<p>
					In the winter of 1941/1942, the forces of the Volkhov Front held an important base area on the western bank of the River Volkhov, where fuel was transported down ice roads. But in the beginning of spring the ice started to melt and fuel transporters constructed an original pipeline. They used pipes from the depot pipelines and connected them with each other by means of rubber-coated flexible joints. Thus they managed to solve the problem of transporting fuel across the Volkhov, under an unceasing enemy fire. 					</p>
					<p>
					That is why the very idea of creating a pipeline under Lake Ladoga undoubtedly occurred to many people involved in the transportation of oil products. A pipeline of this sort could function around the clock in any season and was much less vulnerable to the enemy's strikes. But virtuous wishes are one thing and a practical decision is totally another. After all, a real pipeline many kilometres long had to be laid under Ladoga. Significant underwater investigation was needed to ascertain the geological and hydrological conditions on the line.					</p>
					<p>
					The environment at the bottom of the lake was best known to specialists of EPRON - the well-known Special Expedition for Underwater Works. It was they who laid a telephone cable on the bottom of Ladoga so as to connect Leningrad with Moscow. They also invented the unique technology of transporting oil products using floating railway tanker cars. This meant that the tanker cars were brought via a railway line to the edge of water. The tracks went straight down into the lake and the tanker cars started to sway gently, pulling away from their wheel bases. The "train" of tanker cars rocking on the waves was headed by a tow boat, which pulled them to the other bank of Ladoga where they, still under water, were put on the tracks and pulled out on to the railway line. So EPRON experts never lacked imagination. 					</p>
					<p>
					This work was directed by the first female diver in the USSR, Nina Sokolova. A graduate of Leningrad Water Transportation Institute, the engineer Sokolova didn't just want to supervise the divers from the surface - she wanted to take part in the underwater works herself. She started her diving career in Sochi back in 1937. 					</p>
					<p>
						Nowadays, in the era of aqualungs, it is difficult to imagine how hard and dangerous the work was with heavy diving equipment on. A diver didn't float in the water, and actually there was little benefit in that - he or she stood on the bottom and did a complicated and hard job. The work was directed by engineers who stayed on the surface and told the divers what to do. Nina Sokolova dived herself unlike her land-bound colleagues, saw the underwater objects "on the spot" and knew the limits of what was possible better than anyone.
					</p>
					<p>
					So by the spring of 1942 Sokolova was already considered an experienced professional who had the ear of the top brass. This young and seemingly fragile woman also possessed a determined character and, as they say now, thought outside the box. And one day in February 1942, going to Moscow to visit one of the meetings, she shared her idea with some higher officials. To her mind, the divers could build such a pipeline in no time. The question was, how to put the pipes together. 					</p>
