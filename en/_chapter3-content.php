					<h2 class="h2 title js-title" id="sum">
					The Sum of Technologies
					</h2>
					<p>
					But in fact, the idea of building an underwater pipeline through the south of Lake Ladoga also occurred to the old friends, Shinberg and Khromov, who were still working for Nefteprovodproekt Trust, which had close contacts with the Red Army's Fuel Service. Sokolova's plan confirmed that the pipeline could be buried on the bottom of the lake, and the Khromov-Shinberg project was elaborated well enough technically as well as technologically. 					</p>
					<p>
					But it would be a mistake to suppose that everyone backed the idea. It was usual for a pipeline to cross a river mounted on the truss of a railway bridge. It was known that on the Sakhalin island, a pipeline a little less than 10 km long was successfully laid under the waters of the Strait of Tartary on March 22. But they welded the sea part of the pipeline when it was still on the surface, and only then put it under water. This was impossible on Lake Ladoga because of the longer length of the line and the inevitable air attacks of the enemy. And also, there was no experience of laying a pipeline at such a distance in open water. 					</p>
					<p>
					So when the idea of an underwater pipeline was made public at the Fuel Service Administration, many experts labelled it at once a pipe dream. Nevertheless, the Head of Administration, General Mikhail Kormilitsin, saw fit to delegate to his subordinates ways of figuring out how to build this line and its potential practicability. 					</p>
					<p>
					As David Schinberg later remembered, there were more questions than answers when the decision to build the pipeline was made. It wasn't clear if they had the necessary quantity of pipes for a start. How shall they connect the pipes with each other: by thread or by weld, and if they choose welding, then what shall they choose - gas or electric welding? How to do this literally in the eye of the enemy? Which tasks could be done in the light of day, and which ones only at night?					</p>
					<p>
					Every time Shinberg met officials they were of a higher rank than before. The next report was to be delivered to Anastas Mikoyan, who was in charge of the whole Red Army's supplies. Mikoyan was the ultimate authority before Stalin, and a report to him was very important. It turned out that the report was delivered by Shinberg, and General Khrulev, who was in command of the rear, inserted some additions. But Mikoyan and Khrulev weren't satisfied with the planned construction time - three months, because the amount of fuel left in Leningrad was only enough for 60 days at most. They agreed that the pipeline was to be built within 50 days. Executives and representatives were rapidly appointed, and David Shinberg took up the post of Head Engineer of the project. A project which to all intents and purposes did not exist. 					</p>
