					<h2 class="h2 title title--start js-title" id="pipeline">
					Fast Preparation Pipeline
					</h2>
					<p>
					Following the development of the Red Army's armaments in the beginning of the 1930s, there was a demand for a rapid improvement in fuel supplies for motorised units. The army was interested in all the latest innovations and it was no wonder that in 1933 the Fuel Supply Office ordered Nefteprovodstroi Trust to design a quick-assembling (as well as quick-disassembling) pipeline for transporting fuel in field conditions. The work was delegated to the young experts Schinberg and Khromov under the guidance of the trust director Georgy Rogachev. 					</p>
					<p>
					The sectional field pipeline had to be 75 mm in diameter and up to 100 km long. About 350 tons of fuel per day had to be pumped through it. The young engineers invented a modular-type pipeline. It consisted of sections 10 km long and delivery measuring stations mounted on cars. 					</p>
					<p>
					A year after the beginning of design activities they were ordered to build a preproduction section of the pipeline 2 km long. The pipeline was to be made of special lightweight pipes which had to be connected by specially designed sleeve joints. In 1935, experts of Nefteprovodstroi Trust together with the army ran petrol transportation tests on the pipeline in Armavir. The army liked the project but the costs were too high because a very small number of lightweight pipes was produced in the USSR and they were quite expensive. 					</p>
					<p>
					Eventually, the Red Army's Fuel Service put the so-called "sectional depot pipeline" into operation at the beginning of 1937. It was much shorter than even the preproduction section designed by Schinberg and Khromov - only 1.5 km long. Still it was a real step forward. 					</p>
					<p>
					Four years later, when Hitler's tank convoys were breaking the Red Army's lines of communication, it became clear that one of the most important supply problems was to rapidly deliver fuel to the functioning armoured units and to refuel them just as rapidly. So in August, 1941, the engineers Shinberg and Khromov wrote to People's Commissar for Oil Industry Ivan Sedin suggesting their 1935 project be revived. 					</p>
					<p>
					The Commissar instantly supported the engineers and funds were allocated for improving the project and building the first pipeline. The situation on the front line in the autumn of 1941 didn't inspire any experiments with new technologies, so the project was postponed. But what couldn't be implemented within active moving warfare turned out to be much in demand in a siege environment. Only the pipes had to be laid not on the surface but under water... 					</p>
