					<p>
					But Italian MAS naval launches began operations on Lake Ladoga three days later. They were relocated purposely on German warships to Finland and further - using the railway, to impede water transportation of Soviet cargoes across Ladoga. Besides, the Germans forwarded 6 combat launches to Lake Ladoga in May, each one armed with two AA rapid-fire guns and bearing underwater mines. 					</p>
					<p>
					Landing craft armed with 88 mm main guns and 20 mm AA rapid-fire guns were brought to the location after a short while. Thus, that same summer of 1942 a joint Finnish, Italian and German flotilla consisting of more than 40 battle units (not counting sixty communication support boats) started operating against the Soviet lines of communication on Ladoga. 					</p>
					<p>
					The Luftwaffe also constantly delivered air strikes along the route. But nevertheless the underwater pipeline functioned without failure. 50 thousand tons of fuel were delivered down this pipeline and it was switched off only after the lifting of the siege. 					</p>
					<p class="blockquote">
					Captured documents revealed that the Germans didn't know about the pipeline and its role in supplying the defenders of the city with fuel. 					</p>
					<p>The experience of building the Ladoga pipeline was used almost immediately.
					</p>
					<p>
					Sectional field pipelines became more popular for transferring fuel across small water barriers. And already in April 1943, a more serious task arose. The EPRON experts from Lake Ladoga and Sakhalin were invited to complete this task - they had to lay a long-distance pipeline across the Volga. Pipelines were laid across the Dnieper in 1944 and the Vistula in 1945. In total, more than 1200 km of pipelines were laid during the war, and many of them across water barriers. 					</p>
