					</h2>
					<p>
					The first section of the Ladoga pipeline was welded at the beginning of May, 1942, by an experienced welder, Grigory Lomonosov. His work was recalled by many people who were there at the time, and David Shinberg told this story in his memoirs twenty years later. As Shinberg stated, this was a brilliant job of wonderful quality, and it was fast, too. Lomonosov didn't make a secret of his method and had become a tutor for his colleagues. 					</p>
					<p>
					In early May the welders could already weld two sections 300 m long per day. The pipes were cleaned, scrupulously covered with bitumen coating and tested with the help of kerosene under pressure of 35 atm, that is, 15 times higher than the pressure in car tyres.					</p>
					<p>
					Although section assembly was started at the beginning of May, the laying of the pipeline itself across the lake began only three weeks later. On the one hand there still was some ice on Ladoga in early May, on the other hand Shinberg saw fit to prepare sections beforehand so as to do the most dangerous "water" task as quickly as possible. 					</p>
					<figure>
						<img src="img/chapter3/chapter3__5.jpg" class="figure__img">
					</figure>
					<p>
					By May 25, everything was ready for the beginning of the pipe laying. According to the designed technology, when a section was ready, it had to be pulled down to the lake about 50 m apart while fixing the head end on the pontoon, and the pontoon itself took the end from the tow boat. The tow boat then started pulling the section away from the bank. When the rear end of the section was already on the shore front, it was connected with the next section and the procedure had to be repeated. All this resulted in the so-called "stalk" which was up to 1.5 km long, welded on both ends and held afloat not only with the help of pontoons, but also logs tied to the pipe on both sides. 					</p>
					<p>
					But on May 26, the very day of laying the first stalk, the weather foiled all the plans. A sudden storm carried the tow boat with the stalk southwards, the stalk was moved in parallel to the bank, pontoons and logs were torn off and the stalk sank. All the diver's efforts to find it were unsuccessful - Lake Ladoga hid the precious pipes. 					</p>
